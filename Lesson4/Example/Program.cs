/* 
примеры создания строк в C#
*/
string s1 = "Hello"; // В переменную s1 строкового типа записано значение Hello

char[] chr_array = {'w', 'o', 'r', 'l', 'd'}; // В строковый массив chr_array записаны элементы  w o r l d
string s2 = new String(chr_array); // Строковой переменной s2 присваивается значение из строкового массива chr_array 

string s3 = new String('a', 6); // Строковой переменной s3 присваивается значение из 6 повторяющихся символов 'a' 
